<script>
    function assign_room(booking_id) {
        // Set the booking_id in the hidden input field in the modal
        document.querySelector('#assign_room_form input[name="booking_id"]').value = booking_id;
    }

    function cancel_booking(booking_id) {
        // Set the booking_id in the hidden input field in the modal
        document.querySelector('#cancel-booking_form input[name="booking_id"]').value = booking_id;
    }

    function showAlert(type, msg) {
        const bs_class = (type === "success") ? "alert-success" : "alert-danger";

        const alertDiv = document.createElement('div');
        alertDiv.className = `container mt-3 fixed-bottom`;
        alertDiv.innerHTML = `
            <div class="row justify-content-center">
                <div class="col-lg-6">
                    <div class="alert ${bs_class} alert-dismissable fade show custom-alert" role="alert">
                        <strong class="me-3">${msg}</strong>
                    </div>
                </div>
            </div>
        `;

        document.body.appendChild(alertDiv);

        // Dismiss the alert after 3 seconds
        setTimeout(function(){
            alertDiv.remove(); // Remove the alert
        }, 3000);
    }

    // Handle form submission for assigning a room
    document.getElementById('assign_room_form').addEventListener('submit', function(event) {
        event.preventDefault(); // Prevent the default form submission

        const formData = new FormData(this);

        fetch('assign_room.php', {
            method: 'POST',
            body: formData
        })
        .then(response => response.text())
        .then(data => {
            // Close the modal
            var assignRoomModal = new bootstrap.Modal(document.getElementById('assign-room'));
            assignRoomModal.hide();

            if (data === 'success') {
                showAlert('success', 'Room assigned successfully!');
                location.reload(); // Reload the page to see the changes
            } else {
                showAlert('danger', 'Error: ' + data);
            }
        })
        .catch(error => {
            console.error('Error:', error);
            showAlert('danger', 'An error occurred. Please try again.');
        });
    });

    // Handle form submission for cancelling a booking
    document.getElementById('cancel-booking_form').addEventListener('submit', function(event) {
        event.preventDefault(); // Prevent the default form submission

        const formData = new FormData(this);

        fetch('cancel_booking.php', {
            method: 'POST',
            body: formData
        })
        .then(response => response.text())
        .then(data => {
            // Close the modal
            var cancelBookingModal = new bootstrap.Modal(document.getElementById('cancel-booking'));
            cancelBookingModal.hide();

            if (data === 'success') {
                showAlert('success', 'Booking cancelled successfully!');
                location.reload(); // Reload the page to see the changes
            } else {
                showAlert('danger', 'Error: ' + data);
            }
        })
        .catch(error => {
            console.error('Error:', error);
            showAlert('danger', 'An error occurred. Please try again.');
        });
    });
</script>






